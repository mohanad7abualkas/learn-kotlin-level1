fun main (arg: Array<String>) {

    // الـ Byte نوع من أنواع الـDataType يقبل أرقام بين 128 و -128
    var b: Byte = 11
    println(b)

    // الـ Short نوع من أنواع الـDataType يقبل أرقام أكبر من التي يقبلها الـ Byte
    var a: Short = 202

    // الـ Int نوع من أنواع الـDataType يقبل أرقام كبيرة جدا
    var c: Int = 156165416

    // الـ Long نوع من أنواع الـDataType يقبل أرقام أكبر من التي يقبلها الـ Int
    var x: Long = 999999999999999

    // جميع أنواع الـ DataType السابقة هي لتخزين الأرقام الصحيحة فقط


    // الـ Float نوع من أنواع الـDataType يتم تخزين أرقام عشرية فيه
    var f: Float = 3.9f


//    الـ Double نوع من أنواع الـDataType يتم تخزين أرقام عشرية فيه,
//    ولكنه يفرق عن الـ Float بأنه يمكن إضافة أكثر من رقم بعد الفاصلة 2.892
    var d: Double = 2.548

}

